package javaInterviewQs;

public class QSwitchCase {

	public static void main(String[] args) {
		int a=15;
		 switch (a) {
		case 10:
			System.out.println("11");
			break;
		case 11:
			System.out.println("12");
			break;
		case 13:
			System.out.println("12");
			break;

		default:System.out.println("None");
			break;
		}

	}

}
